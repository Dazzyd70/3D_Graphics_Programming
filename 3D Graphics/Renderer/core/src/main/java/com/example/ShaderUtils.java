public class ShaderUtils {

    public static int loadShader(int type, String fileName) {
        String shaderSource = "";
        try {
            shaderSource = new String(Files.readAllBytes(Paths.get(fileName)), StandardCharsets.UTF_8);
        } catch (IOException e) {
            System.err.println("Could not read shader file: " + fileName);
            e.printStackTrace();
            System.exit(-1);
        }
        
        int shaderID = GL20.glCreateShader(type);
        GL20.glShaderSource(shaderID, shaderSource);
        GL20.glCompileShader(shaderID);

        // error check
        int success = GL20.glGetShaderi(shaderID, GL20.GL_COMPILE_STATUS);
        if (success == GL11.GL_FALSE) {
            String infoLog = GL20.glGetShaderInfoLog(shaderID);
            System.err.println("Error compiling shader (" + fileName + "): " + infoLog);
            System.exit(-1);
        }

        return shaderID;
    }

    public static int createProgram(int... shaders) {
        int programID = GL20.glCreateProgram();
        for (int shader : shaders) {
            GL20.glAttachShader(programID, shader);
        }
        
        GL20.glLinkProgram(programID);

        // error check in linking
        int success = GL20.glGetProgrami(programID, GL20.GL_LINK_STATUS);
        if (success == GL11.GL_FALSE) {
            String infoLog = GL20.glGetProgramInfoLog(programID);
            System.err.println("Error linking shader program: " + infoLog);
            System.exit(-1);
        }

        // detatch and delete shaders
        for (int shader : shaders) {
            GL20.glDetachShader(programID, shader);
            GL20.glDeleteShader(shader);
        }
        
        return programID;
    }
}
